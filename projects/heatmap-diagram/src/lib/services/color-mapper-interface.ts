export interface RGBA {
  /** Red component, value between `0` and `255` */
  r: number;
  /** Green component, value between `0` and `255` */
  g: number;
  /** Blue component, value between `0` and `255` */
  b: number;
  /** Alpha Transpancey, 2 digit float between `0` and `1`, e.g. `0.4` */
  a: number;
}
